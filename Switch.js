console.log("Practising Switch Statement :");
let day_number=5;
switch(day_number){
case 1:
console.log("It is monday");
break;
case 2:
console.log("It is tuesday");
break;
case 3:
console.log("It is wednesday");
break;
case 4:
console.log("It is thursday");
break;
case 5:
console.log("It is friday");
break;
case 6:
console.log("It is saturday");
break;
case 7:
console.log("It is sunday");
break;

default:
    console.log("You have entered a wrong day number.");
}